    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        //salvar
        if (id == 0) {
            if (TestarPreenchimento().equals("")) {
                Doenca tmpDoenca = new Doenca();
                tmpDoenca.setNome(tfdNome.getText());
                tmpDoenca.setCid(tfdCid.getText());
                String retorno = DoenDAO.salvar(tmpDoenca);
                if (retorno.length() > 6) {
                    JOptionPane.showMessageDialog(null, retorno);
                } else {
                    btnSair.setText("Sair");
                    JOptionPane.showMessageDialog(null, "Salvo");
                    btnProcurar.setEnabled(true);
                    btnSalvar.setEnabled(false);
                    btnAlterar.setEnabled(false);
                    btnNovo.setEnabled(true);
                    ligarCampos(false);
                    btnProcurar.setEnabled(true);
                    limpaCampos.limparCampos(jPanel2);
                    tfdCid.setText("");

                }
            } else {
                JOptionPane.showMessageDialog(null, "Insira os Campos:\n " + TestarPreenchimento());
            }

        } else //alterando
        {
            if (TestarPreenchimento().equals("")) {
                Doenca tmpDoenca = new Doenca();
                tmpDoenca.setNome(tfdNome.getText());
                tmpDoenca.setCid(tfdCid.getText());
                tmpDoenca.setId(id);
                String retorno = DoenDAO.atualizar(tmpDoenca);
                if (retorno.length() > 6) {
                    JOptionPane.showMessageDialog(null, retorno);
                } else {
                    btnSair.setText("Sair");
                    JOptionPane.showMessageDialog(null, "Salvo");
                    btnProcurar.setEnabled(true);
                    btnSalvar.setEnabled(false);
                    btnAlterar.setEnabled(false);
                    btnNovo.setEnabled(true);
                    ligarCampos(false);
                    btnProcurar.setEnabled(true);
                    limpaCampos.limparCampos(jPanel2);
                    tfdCid.setText("");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Insira os Campos:\n " + TestarPreenchimento());
            }
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnSairActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSairActionPerformed
        if (btnSair.getText().equals("Sair")) {
            id = 0;
            this.dispose();
        } else {
            id = 0;
            limpaCampos.limparCampos(jPanel2);
            ligarCampos(false);
            btnSair.setText("Sair");
            btnNovo.setEnabled(true);
            btnProcurar.setEnabled(true);
        }
    }//GEN-LAST:event_btnSairActionPerformed

    private void btnNovoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNovoActionPerformed
        limpaCampos.limparCampos(jPanel2);
        id = 0;
        ligarCampos(true);
        btnProcurar.setEnabled(false);
        btnSalvar.setEnabled(true);
        btnNovo.setEnabled(false);
        btnSair.setText("Cancelar");
    }//GEN-LAST:event_btnNovoActionPerformed

    private void btnProcurarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcurarActionPerformed
        limpaCampos.limparCampos(jPanel2);
        procurarDoenca procurar = new procurarDoenca(null, true);
        tmpDoenca = procurar.retornarDoenca();
        popularTela(tmpDoenca);
        btnAlterar.setEnabled(true);
    }//GEN-LAST:event_btnProcurarActionPerformed

